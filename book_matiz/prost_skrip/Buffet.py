Buffet = ('fried eggs ', 'borscht ', 'burger ','potatoes ')  # Создание кортежа
for table in Buffet:  # Перебор кортежа
    print(table.title())  # title делает заглавные буквы
Buffet = ("anaconda", "cow", "matrix")  # Перезначение переменной
for table_now in Buffet:
    print("\n"+table_now.title())
